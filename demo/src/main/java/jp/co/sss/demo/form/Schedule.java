package jp.co.sss.demo.form;

public class Schedule {
	private String ge;
	private String ka;
	private String su;
	private String mo;
	private String ki;
	private String doyou;
	private String ni;

	public String getGe() {
		return ge;
	}
	public void setGe(String ge) {
		this.ge = ge;
	}
	public String getKa() {
		return ka;
	}
	public void setKa(String ka) {
		this.ka = ka;
	}
	public String getSu() {
		return su;
	}
	public void setSu(String su) {
		this.su = su;
	}
	public String getMo() {
		return mo;
	}
	public void setMo(String mo) {
		this.mo = mo;
	}
	public String getKi() {
		return ki;
	}
	public void setKi(String ki) {
		this.ki = ki;
	}
	public String getDoyou() {
		return doyou;
	}
	public void setDoyou(String doyou) {
		this.doyou = doyou;
	}
	public String getNi() {
		return ni;
	}
	public void setNi(String ni) {
		this.ni = ni;
	}
}
